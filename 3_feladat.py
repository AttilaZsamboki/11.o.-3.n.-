# Összeadásjel működése
a = 5
b = 3
c = a + b
print(c)    # eredmény: 8

s1 = "hello"
s2 = "world"
s3 = s1 + s2
print(s3)   # eredmény: "helloworld"

# Szorzásjel működése
x = 2
y = 3
z = x * y
print(z)    # eredmény: 6

s = "abc"
s = s * 3
print(s)    # eredmény: "abcabcabc"

